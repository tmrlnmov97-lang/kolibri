-- ============================================================
--  ЧАСТЬ 1. Тексты и фото страниц (site_content)
-- ============================================================
create table if not exists public.site_content (
  id         uuid primary key default gen_random_uuid(),
  page       text not null,                       -- страница: home, menu…
  block_key  text not null,                       -- ключ элемента: hero_title…
  type       text not null default 'text',        -- text | textarea | image
  section    text not null default '',            -- группа для админки
  label      text not null default '',            -- подпись поля в админке
  value      text,                                -- сам текст или ссылка на фото
  sort_order integer not null default 0,
  unique(page, block_key)
);

alter table public.site_content enable row level security;

drop policy if exists "content public read" on public.site_content;
drop policy if exists "content auth write"  on public.site_content;

create policy "content public read" on public.site_content for select using (true);
create policy "content auth write"  on public.site_content for all to authenticated using (true) with check (true);

-- ============================================================
--  ЧАСТЬ 2. Блюда меню (menu_items)
-- ============================================================
create table if not exists public.menu_items (
  id           uuid primary key default gen_random_uuid(),
  category     text    not null default 'Основное',
  name         text    not null,
  description  text,
  price        integer,
  photo_url    text,
  is_available boolean not null default true,
  sort_order   integer not null default 0,
  created_at   timestamptz not null default now()
);

alter table public.menu_items enable row level security;

drop policy if exists "menu public read" on public.menu_items;
drop policy if exists "menu auth write"  on public.menu_items;

create policy "menu public read" on public.menu_items for select using (true);
create policy "menu auth write"  on public.menu_items for all to authenticated using (true) with check (true);

-- ============================================================
--  ЧАСТЬ 3. Политики для хранилища фото (бакет photos)
-- ============================================================
drop policy if exists "photos public read" on storage.objects;
drop policy if exists "photos auth write"  on storage.objects;

create policy "photos public read" on storage.objects for select using (bucket_id = 'photos');
create policy "photos auth write"  on storage.objects for all to authenticated using (bucket_id = 'photos') with check (bucket_id = 'photos');

